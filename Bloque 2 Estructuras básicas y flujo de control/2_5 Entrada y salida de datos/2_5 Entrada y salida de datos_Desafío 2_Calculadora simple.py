print (f"Vamos a calcular diferentes operaciones a partir de los números que ingreses")  # Mensaje de bienvenida

# Pedimos al usuario que ingrese un número y luego otro número
# Se utiliza la función "int" para que el valor ingresado se convierta en un número entero
# La función "input" permite solicitar información al usuario

numero1 = int(input("Ingrese un número: "))  # Se declara la variable numero1 y se almacena el primer valor ingresado
numero2 = int(input("Ingrese otro número: "))  # Se declara la variable numero2 y se almacena el segundo valor ingresado

# Se declaran las variables para las diferentes operaciones
# Realizamos las operaciones matemáticas y almacenamos los resultados en variables
suma = numero1 + numero2  # Se calcula la suma de numero1 y numero2
resta = numero1 - numero2  # Se calcula la resta de numero1 y numero2
multiplicacion = numero1 * numero2  # Se calcula la multiplicación de numero1 y numero2
division = numero1 / numero2  # Se calcula la división de numero1 y numero2

# Se muestran los resultados de las operaciones
# Cada resultado hace referencia a una variable diferente que contiene el resultado de la operación
print(f"La suma es: ", suma)  # Imprime el resultado de la suma
print(f"La resta es: ", resta)  # Imprime el resultado de la resta
print(f"La multiplicación es: ", multiplicacion)  # Imprime el resultado de la multiplicación
print(f"La división es: ", division)  # Imprime el resultado de la división

# Paso 4: Mensaje final
print (f"¡Gracias por usar el programa! Te espero para el próximo desafío")  # Mensaje de salida

