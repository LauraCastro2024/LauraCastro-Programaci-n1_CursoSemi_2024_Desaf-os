# Pedimos al usuario que ingrese su nombre
# Se utiliza la función "input" para solicitar información al usuario
nombre = input("Ingrese su nombre: ")  # Se declara la variable nombre y se almacena el valor ingresado

# Pedimos al usuario que ingrese su ciudad de origen
# Se utiliza la función "input" para solicitar información al usuario
ciudad = input("Ingrese su ciudad de origen: ")  # Se declara la variable ciudad y se almacena el valor ingresado

# Imprimimos el saludo con esa información
# Usamos print y formateo de cadena (f-string) para mostrar el mensaje personalizado
print(f"Hola, {nombre}! Envíale un saludo a tus compañeros de la ciudad de {ciudad}")  # Mensaje de salida